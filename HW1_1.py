ch = str(input('Please enter character to be checked: '))

if (ch == 'A' or ch == 'a' or ch == 'E' or ch == 'e' or ch == 'I' or ch == 'i' 
or ch == 'O' or ch == 'o' or ch == 'U' or ch == 'u'): 
    print ('The character ', ch ,' is a vowel')
else:
    print ('The character ', ch, ' is a consonant')